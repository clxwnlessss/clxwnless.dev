function openTab(evt, tabName) {
    var tabContent = document.getElementsByClassName("tab-content");
    for (var i = 0; i < tabContent.length; i++) {
        tabContent[i].style.display = "none";
        tabContent[i].classList.remove("active-tab");
    }

    var navBtns = document.getElementsByClassName("nav-btn");
    // –ú–∏ –Ω–µ —á—ñ–ø–∞—î–º–æ –∫–Ω–æ–ø–∫—É –º–æ–≤–∏, —Ç–æ–º—É —ñ—Ç–µ—Ä—É—î–º–æ –ª–∏—à–µ –ø–æ –∫–Ω–æ–ø–∫–∞—Ö –ü—Ä–æ –º–µ–Ω–µ/–ú–∞–≥–∞–∑–∏–Ω
    for (var i = 0; i < navBtns.length; i++) {
        if (!navBtns[i].closest('.language-selector')) {
             navBtns[i].classList.remove("active");
        }
    }

    document.getElementById(tabName).style.display = "block";
    
    setTimeout(() => {
        document.getElementById(tabName).classList.add("active-tab");
    }, 10);

    // –î–æ–¥–∞—î–º–æ –∫–ª–∞—Å "active" –ª–∏—à–µ –∫–Ω–æ–ø–∫–∞–º –ü—Ä–æ –º–µ–Ω–µ/–ú–∞–≥–∞–∑–∏–Ω
    if (!evt.currentTarget.closest('.language-selector')) {
        evt.currentTarget.classList.add("active");
    }
}

