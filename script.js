function setLanguage(lang) {
    const elementsToTranslate = document.querySelectorAll('[data-key]');
    const langData = translations[lang];

    elementsToTranslate.forEach(element => {
        const key = element.getAttribute('data-key');
        if (langData[key]) {
            // –û–Ω–æ–≤–ª–µ–Ω–Ω—è —Ç–µ–∫—Å—Ç—É –µ–ª–µ–º–µ–Ω—Ç–∞
            element.textContent = langData[key];
        }
    });
    
    // –°–ø–µ—Ü—ñ–∞–ª—å–Ω–∞ –æ–±—Ä–æ–±–∫–∞ –¥–ª—è title —Å—Ç–æ—Ä—ñ–Ω–∫–∏ (–æ—Å–∫—ñ–ª—å–∫–∏ title –Ω–µ –º–∞—î .textContent)
    document.title = translations[lang]['page_title'];
    
    // –ó–±–µ—Ä–µ–∂–µ–Ω–Ω—è –æ–±—Ä–∞–Ω–æ—ó –º–æ–≤–∏ —É –ª–æ–∫–∞–ª—å–Ω–æ–º—É —Å—Ö–æ–≤–∏—â—ñ
    localStorage.setItem('websiteLang', lang);
    
    // –û–Ω–æ–≤–ª–µ–Ω–Ω—è –∞—Ç—Ä–∏–±—É—Ç—É lang –¥–ª—è HTML (–¥–ª—è SEO)
    document.documentElement.setAttribute('lang', lang);
}

